class Bernard():
    def __init__(self, DEF, Attack, Synopsis):
        self.Synopsis = Synopsis
        # Entre 1 et 10
        self.Attack = Attack
        # entre 1 et 100
        self.DEF = DEF

    def DEF(self):
        return self.DEF

    def Attack(self):
        return self.Attack

    def degat_subit(self, degat_subit):
        self.DEF -= degat_subit
        print("Bernard à subit", degat_subit, "dégat")

